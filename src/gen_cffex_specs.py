def main():
    print("CFFEX 中金所 合约规格 + 主力日线\n")

    for name, spec in CFFEX_SPECS.items():
        # Generate spec .md
        content = generate_md(name, spec)
        filepath = DOCS_DIR / f"{name}.md"
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)

        # Download main contract data
        rows = 0
        try:
            df = ak.futures_zh_daily_sina(symbol=spec["sina_code"])
            df.columns = ["date", "open", "high", "low", "close", "volume", "hold", "settle"]
            data_path = DOCS_DIR / "main_contracts" / f"{spec['code']}_main.csv"
            df.to_csv(data_path, index=False, encoding="utf-8-sig")
            rows = len(df)
        except Exception as e:
            pass

        print(f"  [{spec['category']}] {name}({spec['code']}): 规格✓ 数据({rows}行)")
        time.sleep(0.3)

    print(f"\nCFFEX: {len(CFFEX_SPECS)} 个品种全部完成")


if __name__ == "__main__":
    main()
