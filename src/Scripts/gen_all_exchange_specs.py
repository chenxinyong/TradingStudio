def download_main_data(name, sina_code):
    """下载主力连续合约日线数据"""
    try:
        df = ak.futures_zh_daily_sina(symbol=sina_code)
        df.columns = ["date", "open", "high", "low", "close", "volume", "hold", "settle"]
        df["date"] = pd.to_datetime(df["date"])
        filepath = DOCS_DIR / "main_contracts" / f"{name}.csv"
        df.to_csv(filepath, index=False, encoding="utf-8-sig")
        return len(df)
    except Exception as e:
        return 0


def main():
    all_specs = {}
    all_specs.update({f"DCE_{k}": v for k, v in DCE_SPECS.items()})
    all_specs.update({f"CZCE_{k}": v for k, v in CZCE_SPECS.items()})
    all_specs.update({f"GFEX_{k}": v for k, v in GFEX_SPECS.items()})

    print(f"生成三大交易所合约规格 + 下载主力日线数据\n")

    total_specs = {"DCE": 0, "CZCE": 0, "GFEX": 0}
    total_data = {"DCE": 0, "CZCE": 0, "GFEX": 0}

    for key, spec in all_specs.items():
        name = key.replace("DCE_","").replace("CZCE_","").replace("GFEX_","")
        exchange = spec["exchange"]

        # Generate spec .md
        content = generate_markdown(name, spec)
        filepath = DOCS_DIR / f"{name}.md"
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        total_specs[exchange] += 1

        # Download main contract data
        if "sina_code" in spec:
            rows = download_main_data(f"{spec['code']}_main", spec["sina_code"])
            if rows > 0:
                total_data[exchange] += 1
                print(f"  [{exchange}] {name}({spec['code']}): 规格✓ 数据({rows}行)")
            else:
                print(f"  [{exchange}] {name}({spec['code']}): 规格✓ 数据✗")
        else:
            print(f"  [{exchange}] {name}({spec['code']}): 规格✓")
        time.sleep(0.3)

    print(f"\n{'='*60}")
    print(f"合约规格: DCE {total_specs['DCE']} + CZCE {total_specs['CZCE']} + GFEX {total_specs['GFEX']} = {sum(total_specs.values())} 个品种")
    print(f"主力数据: DCE {total_data['DCE']} + CZCE {total_data['CZCE']} + GFEX {total_data['GFEX']} = {sum(total_data.values())} 个品种")


if __name__ == "__main__":
    main()
