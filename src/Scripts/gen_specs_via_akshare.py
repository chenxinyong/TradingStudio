def main():
    print("生成有色金属合约规格文档...\n")
    for name, spec in SPECS.items():
        content = generate_markdown(spec)
        filepath = DOCS_DIR / f"{name}.md"
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        # Brief summary
        print(f"  {name}({spec['code']}): {spec['交易单位']}, {spec['合约月份']}, {spec['最小变动价位']}")
        print(f"    → {filepath}")

    print(f"\n完成！{len(SPECS)} 个合约规格已生成")


if __name__ == "__main__":
    main()
